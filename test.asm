TROCA: MACRO A,B
    COPY A,TEMP
    COPY B,A
    COPY TEMP,B
ENDMACRO

INC_SWAP: MACRO X,Y
    LOAD X
    ADD 1
    STORE X
    TROCA X,Y        
ENDMACRO

MAIN: BEGIN
    VAL: CONST 10
    A:   SPACE 1
    B:   SPACE 1
    TEMP: SPACE 1
    BUFFER: SPACE 3      
BUFFER+2

    INC_SWAP A,VAL
    INC_SWAP BUFFER, B   
    OUTPUT A
    OUTPUT B
    OUTPUT BUFFER+2
    STOP
END
